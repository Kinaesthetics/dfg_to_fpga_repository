static void dft(vector<double>& in_real, vector<double>& in_imaginary, vector<double>& out_real, vector<double>& out_imaginary)
{
	int n = in_real.size();
	for(int k =0; k < n; k++)
	{
		double REAL_SUM = 0;
		double IMAGINARY_SUM = 0;
		for(int t = 0; t < n; t++)
		{
			double angle = 2 * PI * t * (k / n);
			REAL_SUM 		+= 	(	(in_real[t] * cos(angle)  )  + ( in_imaginary[t] * sin(angle) ) ); //Implemented on FPGA with an Accumulator
			IMAGINARY_SUM 	+= 	(	(-in_real[t] * sin(angle) )  + ( in_imaginary[t] * cos(angle) ) );
		}
		out_real.push_back(REAL_SUM);
		out_imaginary.push_back(IMAGINARY_SUM);
	}
} 

